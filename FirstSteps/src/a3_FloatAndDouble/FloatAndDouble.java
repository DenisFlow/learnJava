package a3_FloatAndDouble;

public class FloatAndDouble {
    public static void main(String[] args) {
        float myMinFloatNumber = Float.MIN_VALUE;
        float myMaxFloatNumber = Float.MAX_VALUE;
        System.out.println("Float min value " + myMinFloatNumber);
        System.out.println("Float max value " + myMaxFloatNumber);

        double myMinDoubleNumber = Double.MIN_VALUE;
        double myMaxDoubleNumber = Double.MAX_VALUE;
        System.out.println("Double min value " + myMinDoubleNumber);
        System.out.println("Double max value " + myMaxDoubleNumber);

        int myIntValue = 5;
        float myFloatValue = (float)5.24;
        double myDoubleValue = 5.3343443;

    }
}
